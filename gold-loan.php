<script>
    function saveData(e){
    
   // e.preventDefault(); // Prevent form submission
  
  // Get form values
  const goldWeight = parseFloat(document.getElementById('weight').value);
  const expectedAmount = parseFloat(document.getElementById('demanded-amount').value);
  const tenureMonths = document.getElementById('tenure').value;
  
  // Calculate loan amount
  const loanDetails = calculateLoanAmount(goldWeight, expectedAmount, tenureMonths);
   document.getElementById('loan-amount').value = loanDetails;
    }
    

function calculateLoanAmount(goldWeight, expectedAmount, tenureMonths) {

  const goldValuePerGram = 6845; 
  const loanToValueRatio = 0.75;

    const maximumLoanAmount = goldWeight * goldValuePerGram * loanToValueRatio;
        const actualLoanAmount = Math.min(expectedAmount, maximumLoanAmount);
        const interest = (actualLoanAmount * 0.1) * tenureMonths; 
        const totalPayableAmount = actualLoanAmount + interest;
        return totalPayableAmount
}

